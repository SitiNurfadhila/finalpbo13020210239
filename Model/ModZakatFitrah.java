package Model;

public class ModZakatFitrah {
    private int no;
    private String nama;
    private String alamat;
    private int jml_jiwa;
    private int total;

    public int getNo() {
        return no;
    }

    public void setNo(int no) {
        this.no = no;
    }

    public String getNama() {
        return nama;
    }

    public void setNama(String nama) {
        this.nama = nama;
    }

    public String getAlamat() {
        return alamat;
    }

    public void setAlamat(String alamat) {
        this.alamat = alamat;
    }

    public int getJml_jiwa() {
        return jml_jiwa;
    }

    public void setJml_jiwa(int jml_jiwa) {
        this.jml_jiwa = jml_jiwa;
    }

    public int getTotal() {
        return total;
    }

    public void setTotal(int total) {
        this.total = total;
    }
    
    
}
